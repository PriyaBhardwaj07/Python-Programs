# LIST COMPREHENSION
'''
a=[1,2,3,4,5]
print("Before : ", a)
l=[x*2 for x in a]
print("After : ", l)
'''

# Dictionary Comprehension
'''
a=[1,2,3,5,6]
print("Before",a)
l={z:z**2 for z in a} #SQUARE OF NUMBERS
print(l)
'''

# Set Comprehension 
'''
a=[1,2,4,5,6] 
l={j**2 for j in a} # SQUARE OF NUMBERS
print(l) # OUTPUT MAY BE IN ANY FORM AS SETS ARE UNORDERED SO WE GET THE RESULT IN ANY ORDER
'''

